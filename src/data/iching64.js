// 매화역수 시간괘: 4지지 서수로 상·하괘와 동효를 뽑아 64괘 도달
export function deriveIching64(yeonJi, wolJi, ilJi, siJi) {
  const yn = JI_NUM[yeonJi] || 1
  const mn = JI_NUM[wolJi] || 1
  const dn = JI_NUM[ilJi] || 1
  const sn = JI_NUM[siJi] || 1
  const up = ((yn + mn + dn) % 8) || 8
  const low = ((yn + mn + dn + sn) % 8) || 8
  const dong = ((yn + mn + dn + sn) % 6) || 6
  const upT = TRIGRAM8[up], lowT = TRIGRAM8[low]
  const key = `${upT.name}_${lowT.name}`
  const hex = HEX64[key] || HEX64["곤_건"]
  return {
    ...hex,
    symbol: (upT.symbol || "") + (lowT.symbol || ""),
    upO: upT.oh, lowO: lowT.oh, upT: upT.name, lowT: lowT.name,
    upNat: upT.nat, lowNat: lowT.nat, donghyo: dong,
  }
}
