function calcOhaengDist(pillars){
  const dist={木:0,火:0,土:0,金:0,水:0};
  pillars.forEach(p=>{
    const go=normO(GAN_OE[p.gan.ko]);
    const jo=normO(JI_OE[p.ji.ko]);
    if(dist[go]!==undefined)dist[go]++;
    if(dist[jo]!==undefined)dist[jo]++;
  });
  return dist;
}


